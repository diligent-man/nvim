---@param str string
---@return string
local function ascii_preprocessing(logo)
    ---@type table
    local git_dashboard = require("git-dashboard-nvim").heatmap()

    ---@type string
    local prefix = string.rep("\n", 2)

    ---@type string
    local suffix = "\n"

    ---@type table
    logo = split(string.format("%s%s%s", prefix, logo, suffix), "\n")

    local flag = false
    for i = 1, #git_dashboard, 1 do
        if flag then
            table.insert(logo, git_dashboard[i])
        else
            if git_dashboard[i]:len() > 0 then
                if i > 1 then
                    table.insert(logo, git_dashboard[i-1])
                end

                table.insert(logo, git_dashboard[i])
                flag = true
            end
        end
    end
    return logo
end


LogoManager.__index = LogoManager


---@param logo_type string
---@param logo_type number
---@return LogoManager
function LogoManager.new(logo_type, pos)
    ---@type string
    logo_type = logo_type or "ascii"

    ---@type number
    pos = pos or 1

    assert(logo_type == "ascii", "Invalid logo type.")

    if logo_type == "ascii" then
        assert(pos >= 1 or pos <= #LogoManager.__ascii_logos, "Invalid logo position")
    end

    local obj = setmetatable({
        __logo_type = logo_type,
        __pos = pos
    }, LogoManager)

    return obj
end


---@return string
function LogoManager:get_logo()
    if self.__logo_type == "ascii" then
        return ascii_preprocessing(self.__ascii_logos[self.__pos])
    end
end


return {
    LogoManager = LogoManager
}
